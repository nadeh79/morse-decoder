const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
   const chars = expr.match(/.{1,10}/g);
   return chars.map(item => {
    if(item[0] === '*') {
        return ' ';
    }
    const pairs = item.match(/.{1,2}/g);
    let code = '';
    for(let pair of pairs){
        if (pair === '10'){
            code += '.'
        }
        if (pair === '11'){
            code += '-'
        }
    }
    return MORSE_TABLE[code];
   }).join('');
}

module.exports = {
    decode
}
